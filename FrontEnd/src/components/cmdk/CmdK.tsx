export default function CommandK({ }: {}) {
  const [open, setOpen] = React.useState(false);

  React.useEffect(() => {
    const down = (e: KeyboardEvent) => {
      if (e.key === "k" && (e.metaKey || e.ctrlKey)) {
        e.preventDefault();
        setOpen((open) => !open);
      }
    };

    document.addEventListener("keydown", down);
    return () => document.removeEventListener("keydown", down);
  }, []);
  const {setActiveTab} = useActiveTabStore();

  return (
    <CommandDialog open={open} onOpenChange={setOpen}>
      <CommandInput placeholder="Type a command or search..." />
      <CommandList>
        <CommandEmpty>No results found.</CommandEmpty>
        <CommandGroup heading="Suggestions">
          <CommandItem>
            {/* <Calendar className="mr-2 h-4 w-4" /> */}
              <Link href="/">
                <span 
                onClick={() => {setActiveTab("Home"); setOpen(false);}}
                className="font-bold tracking-wider">Home</span>
              </Link>
            <CommandShortcut>âŒ˜J</CommandShortcut>
          </CommandItem>
        </CommandGroup>
        <CommandSeparator />
        <CommandGroup heading="Options">
          <CommandItem>
            {/* <Smile className="mr-2 h-4 w-4" /> */}
              <Link href="/">
                <span 
                onClick={() => {setActiveTab("Arcade"); setOpen(false);}}
                className="font-bold tracking-wider">Arcade</span>
              </Link>
          </CommandItem>
          <CommandItem>
            {/* <Calculator className="mr-2 h-4 w-4" /> */}
              <Link href="/">
                <span 
                onClick={() => {setActiveTab("Achievements"); setOpen(false);}}
                className="font-bold tracking-wider">Achievements</span>
              </Link>
          </CommandItem>
        </CommandGroup>
      </CommandList>
    </CommandDialog>
  );
}
